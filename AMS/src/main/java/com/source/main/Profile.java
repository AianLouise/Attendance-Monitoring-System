    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        Login login = new Login();
        try{
                String sql = "SELECT * FROM tb_account WHERE User_Id = '"+login.id+"';";
               
                ps = conn.prepareStatement(sql);
                rs = ps.executeQuery(sql);
        
                try{
                    sql = "UPDATE tb_account SET name = '"+txt_name.getText()+"' , username = '"+txt_username.getText()+"', email = '"+txt_email.getText()+"', password = '"+txt_password.getText()+"' WHERE User_Id = '"+login.id+"';";

                    ps = conn.prepareStatement(sql);

                    int k = ps.executeUpdate();

                    if(k==1){
                        if(rs.next()){
                            JOptionPane.showMessageDialog(null,"Account Information Updated Successfully");

                        }else{

                            JOptionPane.showMessageDialog(null,"Account Update Failed");
                            txt_password.setText("");
                            txt_email.setText("");
                        }

                    }else{
                        JOptionPane.showMessageDialog(null,"Failed");
                    }

                }catch(SQLException e){
                    JOptionPane.showMessageDialog(null,e);
                }
         }catch(SQLException e){
                JOptionPane.showMessageDialog(null,e);
         }
    }//GEN-LAST:event_btn_saveActionPerformed

