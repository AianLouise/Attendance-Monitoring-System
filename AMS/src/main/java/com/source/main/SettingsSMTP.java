    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        try{
                String sql = "SELECT * FROM tb_account WHERE User_Id='1';";
               
                ps = conn.prepareStatement(sql);
                rs = ps.executeQuery(sql);
        
                try{
                    sql = "UPDATE tb_smtp SET email = '"+txt_email.getText()+"' , password = '"+txt_password.getText()+"' WHERE id = '1';";

                    ps = conn.prepareStatement(sql);

                    int k = ps.executeUpdate();

                    if(k==1){
                        if(rs.next()){
                            JOptionPane.showMessageDialog(null,"SMTP Account hase been Change Successfully");

                        }else{

                            JOptionPane.showMessageDialog(null,"Password Reset Failed");
                            txt_password.setText("");
                            txt_email.setText("");
                        }

                    }else{
                        JOptionPane.showMessageDialog(null,"Registration Failed");
                    }

                }catch(SQLException e){
                    JOptionPane.showMessageDialog(null,e);
                }
         }catch(SQLException e){
                JOptionPane.showMessageDialog(null,e);
         }
    }//GEN-LAST:event_btn_saveActionPerformed

